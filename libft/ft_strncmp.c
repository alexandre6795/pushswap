#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while ((i < n -1) && ((unsigned char)s1[i] != 0)
		&& ((unsigned char)s2[i] != '\0'))
	{
		if ((unsigned char)(s1[i] - s2[i]) == 0)
			i++;
		else
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
